# можно использовать range с нужным шагом, формула приведена для наглядности.
# Она вычисляет n-ый член последовательности. Например, если последовательность
# начинается с 1, шаг равен 2, то 5-й член последовательности будет
# равен 1 + (5 - 1) * 2 = 9. В примере будет 7 + (5 - 1) * 2 = 15.
# a = a1 + (n-1) * d.


start = 7
step = 2
quantity = 5

for member in range(1, quantity+1):
    print(start+(member-1)*step)
